   <h4>   <span class="fa-stack fa-1x">
                              <i class="fa fa-circle fa-stack-2x text-primary"></i>
                              <i class="fa fa-code fa-stack-1x fa-inverse"></i>
                        </span> Ingenieria de sistemas</h4>
                        <p>

La Ingeniería de Sistemas Informáticos abarca conocimientos de Ciencias de Computación, Matemática, Ingeniería de Software y Gestión Empresarial.
</p>
<p>
<h4>Como actividad:</h4> La Ingeniería de Sistemas Informáticos es el arte y la ciencia por medio de la cual se analiza, se diseña, se construye, se implementa, se da mantenimiento y se administran sistemas informáticos complejos; buscando mejorar de manera significativa y continua la eficiencia y la efectividad de los procesos de información del Ser Humano, tales como: solucion de problemas, toma de decisiones y aprendizaje; y con ello, incrementar el nivel de propósito de sus actividades.
</p>

<p><h4>Como disciplina:</h4> La Ingeniería de Sistemas Informáticos comprende un sistema de principios, métodos, técnicas y herramientas derivado del conocimiento de las ciencias de Computación, la Matemática, las Ciencias Naturales, Las Ciencias sociales y la Gestión Empresarial. El conocimiento de esta disciplina se adquiere mediante estudio, experimentación y práctica profesional; y se aplica desarrollando sistemas informáticos complejos con criterios de Ingeniería de manera eficiente, eficaz y con buen juicio.
</p>
<h4>Objetivo</h4>
<p>
Formar ingenieros competentes para aplicar de manera eficiente y efectiva los conocimientos de la Ingeniería de Sistemas Informáticos en la solucion de problemas de su entorno, mediante el análisis, el diseño, la construcción, la implementación, el mantenimiento y la administración de sistemas informáticos complejos; proveyendo mejoras significativas en los procesos pertinentes en beneficio de los usuarios de estos sistemas
</p>

<h4>Perfil de ingreso</h4>
<p>
Las características que idealmente debería reunir el aspirante al programa de Ingeniería de Sistemas Informáticos son las siguientes:
<ul>
 <li>Dominio de la matemática de la educación media: Aritmética, Algebra, Geometría y Trigonometría.</li>
 <li>Dominio de la Física de la educación media.</li>
 <li>Dominio de herramientas de software de oficina para procesamiento de palabras, hojas electrónicas, presentaciones, correo electrónico y navegación por internet</li>
 <li>fuerte inclinación a los procesos analíticos.</li>
 <li>Disposición para el trabajo en grupo y extra clase</li>
 <li>Inclinación a la creatividad y la innovación a través de la aplicación de conocimiento técnico y científico</li>
</ul>
</p>

<h4>Perfil de Egreso</h4>
<p>
El graduado de Ingeniería de Sistemas Informáticos requiere contar con conocimientos y destrezas específicos derivados de su formación académica durante el desarrollo del plan de estudios, a fin de poder aplicar las capacidades y competencias que las organizaciones empleadoras esperan del como Ingeniero Informático. Las expectativas actuales de los empleadores de Ingenieros Informáticos a un nivel geográficamente global se resumen en las siguientes capacidades:
 <ul>
 <li>Mejora de procesos organizacionales.</li>
 <li>Explotación de oportunidades organizacionales derivadas de innovaciones tecnológicas.</li>
 <li>Comprensión y descripción de requerimientos informáticos.</li>
 <li>Diseño y administración de arquitecturas informáticas empresariales.</li>
 <li>Identificación y evaluación de soluciones alternativas y formas de implementación.</li>
 <li>Seguridad de datos, información y control de riesgos de la tecnología informática y de comunicaciones.</li>
 <li>Diseño, construcción y mantenimiento de software informático.</li>
</ul>
</p>

<h4>Áreas curriculares</h4>
<p>
La carrera de Ingeniería de Sistemas Informáticos comprende cuatro áreas curriculares, cuya administración está a cargo de un Departamento de ad hoc:
<ul>
<li>Programación y manejo de datos.
Comprende los fundamentos de programación de computadoras a nivel teórico y practico utilizando computadoras: Técnicas de programación, lenguajes de programación y enfoque algorítmico en el procesamiento de datos.</li>

<li>Comunicaciones y Ciencias de la Computación.
En esta área se discute la tecnología avanzada pertinente a la Ingeniería 
Informática derivada de las Ciencias de Computación (estructura de datos, sistemas operativos, arquitectura de computadoras, etc.); y la tecnología de interconexión en red de componentes de sistemas informáticos al nivel local, amplio y de internet.</li>

<li>Desarrollo de sistemas
Esta área comprende los fundamentos del modelado de sistemas informáticos, buscando desarrollar en el estudiante su capacidad de innovación al analizar y diseñar de tales sistemas en diversos entornos organizacionales, con especial énfasis en bases de datos, soporte a la toma decisiones niveles de una estructura organizativa, inteligencia de negocios y minería de datos.</li>

<li>Administración
En esta área se discuten técnicas y herramientas para la gestión eficiente y eficaz del recurso humano e informático que tiene bajo su responsabilidad un gerente informático o un gerente de proyecto; y para la presentación de servicios de consultoría profesional en el campo de la Ingeniería de Sistemas Informáticos. Son temas de especial relevancia en esta área: modelos de administración de unidades informáticas, seguridad informática, costos informáticos y estándares internacionales de integridad, disponibilidad, confiabilidad y seguridad.</li>

Campos de acción profesional.

El graduado de la carrera de Ingeniería de Sistemas Informáticos estará capacitado para laborar en organizaciones de cualquier índole, incluyendo empresas privadas, instituciones públicas y ONGs. También estará formado para intervenir en procesos de investigación y docencia en el campo de tecnología informática y de comunicaciones, con miras a crear o adaptar tecnologías propias.
algunos de los puestos de trabajo en los cuales podrá desenvolverse son: Gerente Informático, gerente de proyectos informáticos, auditor informático, desarrollador de sistemas informático, administrador de redes y comunicaciones, consultor en ingeniería informática, gerente de seguridad informática, administrado de bases de datos, gerente de inteligencia de negocios, docente e investigador en tecnología informática, arquitecto de sistemas informáticos, gerente de contenidos Web y gerente de pruebas en sistemas informáticos.

</ul>

