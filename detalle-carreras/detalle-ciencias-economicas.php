  <br>
 <h4>Descripcion</h4>
                        <p>
La carrera de contaduría publica pretende una formación integral en el 
profesional que se dedique a utilizar y aplicar los conocimientos técnicos, las Normas Internacionales de Información Financiera y las Normas Internacionales de Auditoria, a una unidad económica individual o 
social en la que se registran, organizan y procesan los eventos financieros para producir informes técnicos para la gerencia y los diversos usuarios de la información financiera que les sirvan a la toma de decisiones.
</p>

<h4>Objetivos de la carrera</h4>

<p>El objetivo de la carrera de Licenciatura en Contaduría Publica es formar profesionales que tengan la capacidad y eficiencia para diseñar, implementar y evaluar sistemas contables manuales o computarizados que contengan normas, pautas, métodos y procedimientos para registrar las operaciones monetarias y suministrar información financiera de una empresa, así como el análisis e interpretación de los Estados Financieros y cualquier tipo de informe derivado del flujo de información, los cuales son importantes para la toma de decisiones en cualquier institución pública o privada. La carrera de Contaduría intenta formar en el Contador Público una visión que le permita saber y deducir el rumbo financiero esperado en la procura de recomendar acciones para ayudar al crecimiento de una unidad económica
</p>
<h4>Perfil de ingreso</h4>
<ul>
 <li>capacidad para el razonamiento analítico y la sistematización de ideas.</li>

<li> inclinación por el orden, aseo y el trabajo en equipo.</li>

<li> Facilidad para organizar manejar cantidades grandes de datos</li>
 
<li>Disposición para el análisis minucioso, prolongado de información financiera</li>

<li>capacidad de observación y análisis</li>

<li>alto sentido de responsabilidad</li>

<li>sensibilidad social</li>

<li>aptitud para el cálculo numérico y financiero</li>

<li>Alto sentido de responsabilidad y superación personal</li>
</ul>

<h4>Perfil de egreso</h4>
<p>El contador público debe estar dispuesto a servir a la sociedad, adquirir competencias de conocimiento técnico y sociales, trabajando y auxiliando en forma directa o indirectamente a la alta dirección, conduciendo su visión de lo operacional a lo estratégico, y que además de cumplir con su función de evaluadores de la información financiera ser conviertan en críticos mismos de estados financieros producidos y sean el soporte para la toma de decisiones oportunas.
</p>
<h4>Áreas de desempeño</h4>
<p>El profesional de la contaduría se puede especializar en las áreas de: </p>
<ul>
<li> Ejercer en los ámbitos de contabilidad, presupuestos, costos, análisis financiero, auditoría financiera y fiscal de manera dependiente o independiente en empresas públicas, privadas y ONGs.</li>
<li> En docencia e investigación</li>
<li>Despachos de contabilidad asesorando en el área fiscal, auditoria o consultoría.</li>
<li>Contador, auditor y analista financiero en entidades del sistema financiero en entidades del sistema financiero como bancos, operaciones de bolsa de valores, arrendadoras, compañías de seguros, empresas de factoraje, etc.</li>
</ul>

<h4>Tiempo de duración</h4>
<p> Cinco años de estudio</p>

<h4>Grado y título que otorga:</h4>
<p> Licenciado(a) en Contaduría Publica  


                        </p>
