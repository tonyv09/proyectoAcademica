      <br><h4>Descripcion</h4>
                        <p>
                            El estudio de la Administración de Empresas promueve la creación y el desarrollo de diversos tipos de organizaciones, desde micro a grandes empresas; públicas, privadas y entidades sin fines de lucro; faculta para la gestión de las mismas de forma eficiente y eficaz, sobre todo en las condiciones actuales como son existir en ambientes inestables y turbulentos. El Salvador y la región centroamericana no están exentos a dichas condiciones, por lo tanto, los profesionales formados están capacitados para hacerles frente y superar los retos que se les presenten.
La formación proporcionada se basa en la reflexión, la crítica orientadora, la crítica creadora, el análisis y la aplicación de técnicas, todos ellos capaces de promover la participación activa y protagónica de los individuos en el desarrollo económico y social del país y la región. Ademas, desarrolla la capacidad de interpretar las estructuras historicas, economicas, sociales y politicas, las cuales son el efecto de eventos dinamicos de realidad, ante los cuales se debe adoptar una posicion cientifica, proporcionando alternativas de solucion y ejecucion.
</p>

<h4>Objetivos de la carrera</h4>
<ul>
<li>Desarrollar conocimientos y capacidades técnicas, en áreas tales como:
    Contabilidad, Finanzas, Presupuestos, Mercadotecnia, Producción,    
    Administración de Recursos Humanos, Formulación y Evaluación de 
    Proyectos.</li>
<li>Formar profesionales capaces de identificar oportunidades y de aplicar sus conocimientos para el desarrollo económico y social del país y la región</li>
<li>
*Formar profesionales capaces de utilizar la tecnología y tomar decisiones adecuadas, luego de simular diversos tipos de opciones.</li>
</ul>
<h4>Perfil de ingreso:</h4>
<p>Los(as) aspirantes pueden ser Bachilleres graduados en cualquier opción o contar con estudios equivalentes debidamente acreditados. Ademas, se espera que posean características y actitudes personales relacionadas con el liderazgo, la honestidad, la responsabilidad, la sensibilidad social y la capacidad analítica
</p>
<h4>Perfil de egreso:</h4>
<p>El(la) profesional en Administración de Empresas estará capacitado de manera integral, con una sólida formación humanística, cientifica y tecnológica, con visión multidisciplinaria de la administración estratégica, es decir, es un especialista en coordinar los esfuerzos humanos y materiales para el logro de los objetivos sociales.</p>

<h4>Campo de acción profesional.</h4>
<p>El(La) profesional en Administración de Empresas se desempeñará de forma eficaz y eficiente en diversos tipos de organizaciones, entre ellas las públicas, las privadas y las entidades sin fines de lucro. Pero lo más importante será su capacidad visionaria para ser emprendedor, capaz de generar acciones y proyectos que beneficien a la sociedad salvadoreña y centro americana.</p>

<h4>Tiempo de duración</h4>
<p> Cinco años de estudio</p>

<h4>Grado y título que otorga:</h4>
 <p>Licenciado(a) en Administración de Empresas

                        </p>
