<h4><p> DESCRIPCIÓN DE LAS AREAS CURRICULARES O DE FORMACIÓN:</p></h4>
                        <p>La Carrera de Ingeniería Agronómica tiene como fin contribuir al desarrollo sostenible de El Salvador, atendiendo las demandas de los diferentes sectores poblacionales preferentemente en beneficio de las mayorías; desarrollar las Ciencias Agronómicas, con un enfoque holístico y sistemático con un conocimiento científico de los procesos y fenómenos de la naturaleza y la sociedad; desarrollar tecnologías apropiadas para mejorar los sistemas de producción agropecuarios, la conservación y el aprovechamiento racional de los recursos naturales renovables y, contribuir con un modelo curricular de contenidos ajustados a la realidad del desarrollo agropecuario en El Salvador.</p>

<p>
La estructura de la Carrera de Ingeniería Agronómica comprende tres ejes:
Teórico, de Investigación y de Proyección Social, los cuales presentan una modalidad integradora de una forma progresivaen el desarrollo del Pensum, de manera que en los primeros años habrán cursos integradores relacionados a la realidad agropecuaria nacional en donde se involucran conocimientos básicos y aspectos socioeconómicos del sector agropecuario, hasta lograr la integración en los últimos años de la Carrera. El Pensum se ha dividido en tres áreas: Un área básica donde conocerán los fundamentos físicos, químicos, matemáticos, biológicos y sociales.
</p>

<p>Un área Tecnológica en donde conocerán los factores productivos del campo agropecuario enfocando principalmente el uso, conservación y manejo de los recursos naturales y el área de Integración en donde se estudiarán los diferentes sistemas de producción agropecuaria en los cuales estarán involucrados los
factores que intervienen en la productividad.
</p>
<p>
En el desarrollo de las tres áreas van involucrados los ejes de Investigación, y Proyección Social los cuales se desarrollarán en los diferentes ciclos del Pensum mediante la realización de actividades que tengan que ver con los problemas reales del sector agropecuario, así como del conocimiento del estado, uso, manejo y conservación de los recursos naturales
                        </p>


<h4>Objetivos de la carrera</h4>
<p>El objetivo central de lacarrera es formar Ingenieros Agronomos con conocimientos cientificos y tecnicos para impulsar el desarrollo agropecuario y forestal, con habilidades y destrezas en le manejo y conservacion y proteccion del medio ambiente y de los recursos naturales.</p>

<h4>Perfil de Ingreso</h4>
<p>Todo estudiante que aspire ingresar a la carrera de Ingenieria Agronomica debera tener vocacion y perseverancia para el estudio de ciencias agricolas y pecuarias; disposicion al trabajo en equipo y disciplina para cumplir con las tareas que le exige la carrera. </p>

<h4>Perfil de Egreso.</h4>
<p>EL ingeniero Agronomo que egresa de la facultad posee conocimentos sobre las ciencias agricolas y pecuarias. Aplica principios y metodos de las ciencias para el manejo sostenible de todos los factores que intervienen en las cadenas de produccion de bienes de consumo alimenticio de la poblacion. Esta capacitado para desenvolverse en el area rural e impulsar sus mejoras optimizando los recursos con que cuenta para desarrollar la economia del pais.
Podra desempeñarse con iniciativa y creatividad en diversos ambientes laborales de su competencia profesional en un marco de respeto y consideracion con sus semejantes.</p>

<h4>Campos de accion profesional</h4>
 <ul>
 <li>Administracion de empresas agropecuarias con productores individuales y cooperativas </li>
 <li>Consultoria y asesoria tecnica en la produccion agropecuaria </li>
 <li>Organizaciones no gubernamentales dedicadas al desarrollo agropecuario. </li>
 <li>Empresas de su propiedad. </li>
  <li>Instituciones del Estado relacionadas con el que hacer agropecuario
</li>
</ul>

<h4>Tiempo de duracion de la carrera.</h4>
<p> Cinco años de estudio.</p>

<h4>Grado y titulo que otorga.</h4>
<p>Ingeniero Agronomo</p>
      


