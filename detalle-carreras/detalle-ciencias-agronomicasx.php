  <h4>Descripcion</h4>
                        <p>
La carrera de ingeniria Agroindustrial responde a la necesidad de preservar la vida
util de los bienes alimenticios que provienen de la produccion agropecuaria, su procesamiento es la manera de mejorar y conservar su calidad por largo tiempo.</p>

<p>
Evitar el deterioro de los productos alimenticios trae enormes ventajas al consumo interno y a la exportacion, ademas al generar valor agregado a los productos se amplia la oferta de empleo el beneficio economico a las familias y al pais en general.
</p>
<h4>Objetivos</h4>
<p>
El objetivo fundamental de la carrera es formar profesionales con capacidades cientificas y tecnicas para contribuir al desarrollo sostenibles de la agroindustria nacional
</p>
<h4>Perfil de Ingreso</h4>
<p> 
El estudiante que opte por la carrera debera tener conocimientos basicos de ciencias naturales, matematicas y humanidades. Voacacion para el estudio, creatividad e iniciativa para desenvolverse en el medio rural y en las etapas de lacadena agroalimenticia.</p>
<h4>Perfil de Egreso </h4>
<p>
Todo profesional de la carrera de Agroindustria posee conocimientos amplios de las ciencias y tecnologias que lo capacitan para participar, con pertinencia, en los procesos de cosecha y transformacion agroindustrial de alimentos utilizando en forma optima los recursos. Ademas, esta preparado para poder dirigir y administrar instituciones dedicadas al procesamiento y conservacion de alimentos, sean estas artesanales o industriales.
</p>
<h4>Campos de accion </h4>
<p>
<ul>
  <li>Administracion de empresas agroindustriales</li>
   <li>consultoria y asesoria tecnica en la produccion agropecuaria, con enfasis en el procesamiento, transformacion y comercializacion de productos agroindustriales.</li>
 <li>investigacion y desarrollo de nuevos productos y procesos agroindustriales y de comercializacion.</li>
 <li>Gestion ambiental de la produccion y procesamiento de los productos agropecuarios</li>

</ul>
